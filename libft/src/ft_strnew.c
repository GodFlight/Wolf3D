#include "libft.h"

char	*ft_strnew(size_t size)
{
	void *rs;

	if (size == (size_t)(-1))
		return (NULL);
	if (!(rs = malloc(size + 1)))
		return (NULL);
	else
	{
		ft_bzero(rs, size + 1);
		return (rs);
	}
}
