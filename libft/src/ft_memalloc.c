#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void *rs;

	if (size == (size_t)(-1))
		return (0);
	if (!(rs = malloc(size)))
		return (NULL);
	else
	{
		ft_bzero(rs, size);
		return (rs);
	}
}
