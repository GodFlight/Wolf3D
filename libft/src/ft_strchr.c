#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*cpy;

	cpy = (char*)s;
	i = 0;
	while (cpy[i] != '\0')
	{
		if (cpy[i] == c)
			return (&cpy[i]);
		i++;
	}
	if (cpy[i] == c)
		return (&cpy[i]);
	return (NULL);
}
